		<h2><span>Historia</span> HMS</h2>
		<p>En el año 1991 ante la inquietud de los Doctores Roberto Sandoval Lujan, Carlos Malja Aguirre y Gerardo Cardenas de construir un Hospital Privado con Atención de Segundo Nivel que satisfaciera las necesidades Médico Hospitalarias de la Región de Cd. Cuauhtémoc, Chihuahua.</p>
        <br>
        <p>Este Grupo de Médicos, junto con otros más y los hermanos Chavira Enriquez, quienes dieron el terreno como aportacion en especie a la sociedad, conformando legalmente el Grupo de inversionistas conformando la Sociedad Médica Denominada "HOSPITAL MEDICA SIERRA" el 9 de Diciembre de 1992.</p>
        <br />
        <p>Se nombra como Presidente del Consejo al Dr. Roberto Sandoval Luján quien encabeza el proyecto colocando la primera piedra el 4 de Marzo de 1994, el proyecto de costrucción dura alrededor de 10 meses, pero debido a una crisis financiera, una devaluación, se ve afectada la apertura inmediata, sin embargo se logra coseguir apoyo del Gobierno del Estado, Banrural y Bancomext para equipamiento y capital de trabajo, y por fin el 10 de Febrero de 1996 es Inaugurado por el Gobernador del Estado el C. Lic. Francisco Barrio Terrazas.</p>
        <br />
        <p>Quedando así conformado el Hospital Médica Sierra ubicado en Nicolas Bravo y 21 de marzo, col. Progreso de Ciudad Cuauhtémoc Chihuahua.</p>
        <br />
        <p>Cumpliendo cabalmente con Nuestra Misión, Visión y Valores, día con día siendo nuestra principal prioridad la Salud y el Bienestar de los habitantes de Nuestro Estado.</p>
